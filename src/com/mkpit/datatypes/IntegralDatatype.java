package com.mkpit.datatypes;

//Integral - Byte,short,int,long
//byte -1 byte
//short-2 byte
//int - 4 byte
//long- 8 byte 
//all integral datatype default value zero

public class IntegralDatatype {

	public static void main(String[] args) {

  byte b1=10;
  short s1=20;
  int a=300;
  long l1=746574567L;//we must used L or l
  
  System.out.println(b1);
  System.out.println(s1);
  System.out.println(a);
  System.out.println(l1);
	
	}

}
