package com.mkpit.datatypes;

//Floating Datatype =Float,Double
//Float=4 byte
//Double=8 byte
//float always give us decimal value or Floating value
//default value 0.0

public class FloatingDatatype {

	static float a1;
	static double a2;
	
	public static void main(String[] args) {
		
		float f1=10.0f;
		double d1=24445645.78;
		
		System.out.println(f1);
		System.out.println(d1);
		
		System.out.println(a1);
		System.out.println(a2);

	}
}
